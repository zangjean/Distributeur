package application.model.distrib.productModel.product.beverage.sugar.all.chocolate;

/**
 * Enumération représentant les différents arômes disponibles pour un chocolat chaud.
 * Chaque arôme peut être ajouté avec un surcoût de 0.30 €.
 */
public enum Aroma {

    /** Arôme vanille. */
    VANILLA,

    /** Arôme noisette. */
    HAZELNUT,

    /** Arôme caramel. */
    CARAMEL,

    /** Arôme amande. */
    ALMOND
}
