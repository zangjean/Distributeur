package application.model.distrib.productModel.product;

/**
 * Enumération représentant les niveaux de Nutriscore pour un produit alimentaire.
 * Le Nutriscore va de A (meilleur score nutritionnel) à E (moins bon score).
 */
public enum Nutriscore {

    /** Nutriscore A - Très bon profil nutritionnel. */
    A,

    /** Nutriscore B - Bon profil nutritionnel. */
    B,

    /** Nutriscore C - Profil nutritionnel moyen. */
    C,

    /** Nutriscore D - Profil nutritionnel faible. */
    D,

    /** Nutriscore E - Mauvais profil nutritionnel. */
    E
}
